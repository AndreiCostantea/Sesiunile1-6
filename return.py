def is_natural(numar):
    if numar >= 0:
        return True
    else:
        return False

if (is_natural(4)):
    print('Numarul este natural')
else:
    print('Numarul nu este natural')

if (is_natural(-4)):
    print('Numarul este natural')
else:
    print('Numarul nu este natural')
